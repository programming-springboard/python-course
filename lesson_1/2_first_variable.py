# Змінні - це іменовані області памʼяті. Змінні в Python повинні обовʼязково 
# ініціалізуватись з якимось значенням.

# * За конвенцією написання Python-коду PEP8, назви змінних мають починатись з
#   маленькою літери, а слова в назві змінній повинні відокремлюватись нижнім
#   підкреслюванням. Цей стиль іменування змінних має назву snake_case.

my_first_variable = "Hello, world!"
print("The value of my first variable is", my_first_variable)

# Змінні, як це не дивно, можуть змінювати своє значення в процесі виконання
# програми.

my_first_variable = "New value for my first variable"
print("The value of my first variable after update is", my_first_variable)
